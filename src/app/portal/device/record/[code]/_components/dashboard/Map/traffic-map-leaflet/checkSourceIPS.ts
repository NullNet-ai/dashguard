// Check if an individual IP is private
export function isPrivateIp(ip: string): boolean {
  const parts = ip.split('.').map(Number)
  if (parts.length !== 4 || parts.some(p => p < 0 || p > 255)) return false

  const [a, b]: any = parts

  if (a === 10) return true
  if (a === 192 && b === 168) return true
  if (a === 172 && b >= 16 && b <= 31) return true

  return false
}

// Function to filter private and public IPs
export function categorizeIps(ipArray: string[]): { privateIps: string[], publicIps: string[] } {
  const privateIps = ipArray.filter(isPrivateIp)
  const publicIps = ipArray.filter(ip => !isPrivateIp(ip))

  return { privateIps, publicIps }
}
